/*鸡的数量x，兔的数量y，
  脚的总数a，头的总数b*/
#include <stdio.h>
void main()
{
	printf("鸡兔同笼问题求解程序\n\n");
	int x,y,a,b;  /*定义x,y,a,b为整型变量*/
	printf("请输入头数:");
	scanf("%d",&a);
	printf("请输入脚数:");
	scanf("%d",&b);
	x=(4*a-b)/2;  /*求鸡数量的数学表达式*/
	y=(b-2*a)/2;  /*求兔数量的数学表达式*/
        if (a <= 1 || b % 2 != 0 || b < 2 * a || b > 4 * a) //无解的情况:1、头的总数小于1；2、脚的总数为奇数；3、脚的总数小于头的两倍;4、脚的总数大于头的四倍。
          printf("无解");
        else
	printf("鸡的数量:%d\n兔的数量:%d\n",x,y);
}